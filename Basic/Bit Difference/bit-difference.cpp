class Solution{
    public:
    // Function to find number of bits needed to be flipped to convert A to B
    int countBitsFlip(int a, int b){
        // Your logic here
        //Done with my own logic on my own
        if(a==b)
        return 0;
        int x=0,count=0;
        while(x<32){
            if((a&(1<<x))!=(b&(1<<x)))
            {
                count++;
            }
            x++;
        }
        return count;
    }
};

