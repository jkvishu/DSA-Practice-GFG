class Solution
{
    public:
    //Function to find the first position with different bits.
    int posOfRightMostDiffBit(int m, int n)
    {
        // Your code here
        //Done on my own with my own logic
        if(m==n)
        return -1;
        int x=0;
        while(x<32){
            if((m&(1<<x))!=(n&(1<<x))){
                return x+1;
            }
            x++;
        }
        return -1;
    }
};

